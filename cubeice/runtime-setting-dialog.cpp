		//  runtime_setting
		/* ----------------------------------------------------------------- */
		int runtime_setting(HWND owner, CubeICE::RuntimeSetting& setting) {
			return DialogBoxParam(GetModuleHandle(NULL), _T("IDD_RUNTIME_COMPRESS"), owner, compress::runtime_setting_wndproc, (LPARAM)(&setting));
		}
